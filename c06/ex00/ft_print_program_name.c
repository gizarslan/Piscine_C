#include <unistd.h>

int	main(int ac, char *argv[]) // **argv
{
	int	i;

	i = 0;
	if (ac > 0)
	{
		while (argv[0][i] != '\0')
		{
			write(1, &argv[0][i], 1);
			i++;
		}
		write(1, "\n", 1);
	}
}
